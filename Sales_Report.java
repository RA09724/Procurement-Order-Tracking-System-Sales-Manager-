    private void displayChart(JPanel chartPanel) {
        // Remove any existing chart and add the new one to the frame
        getContentPane().removeAll();
        setLayout(new BoxLayout(getContentPane(), BoxLayout.Y_AXIS)); // Layout for the chart
        add(chartPanel); // Add the new chart panel to the frame
        revalidate(); // Revalidate the frame to apply changes
        repaint(); // Repaint the frame
    }

