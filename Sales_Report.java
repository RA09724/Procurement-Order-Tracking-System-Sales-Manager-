    private void showChartPopup(String title, JPanel chartPanel) {
    // Create a JDialog for the popup
    JDialog popupDialog = new JDialog();
    popupDialog.setTitle(title); // Set the title
    popupDialog.setModal(true); // Make it a modal dialog (blocks other windows)

    // Add the chart panel to the dialog
    popupDialog.getContentPane().add(chartPanel);
    popupDialog.setSize(800, 600); // Set size of the popup
    popupDialog.setLocationRelativeTo(null); // Center on screen

    // Make the dialog visible
    popupDialog.setVisible(true);
    }
    
